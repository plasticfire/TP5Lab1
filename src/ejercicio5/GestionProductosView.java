    // El boton agregar utiliza variables locales para recuperar los valores de los campos, crear un producto, lo carga y limpia los datos.
    private void jbtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAgregarActionPerformed
        try {
        String categoria = (String)jComboBox1.getSelectedItem();
        String nombre = jtProducto.getText();        
        int precio = Integer.parseInt(jtPrecio.getText());
        Producto produc = new Producto(nombre, categoria, precio);
        cargarProducto(produc);
        jtProducto.setText(null);
        jtPrecio.setText(null);
        } catch (NumberFormatException exc) {
            JOptionPane.showMessageDialog(this, "Debe ingresar los datos solicitados de manera correcta");
        }        
    }//GEN-LAST:event_jbtnAgregarActionPerformed

