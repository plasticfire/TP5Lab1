    // Armamos la cabecera de la tabla seteando el modelo con los valores que queremos de las columnas.
    private void armarCabecera(){        
        modelo.addColumn("Categoria");
        modelo.addColumn("Producto");
        modelo.addColumn("Precio");
        jtTablaProductos.setModel(modelo);
    }
    
    // Creamos las columnas de la tabla pasandoles un producto previamente creado.
    private void cargarProducto(Producto producto){        
        modelo.addRow(new Object[]{producto.getCategoria(),producto.getNombre(), producto.getPrecio()});       
    }
